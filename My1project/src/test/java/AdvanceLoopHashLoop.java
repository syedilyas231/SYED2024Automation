public class AdvanceLoopHashLoop {
    public static void main(String[] args) {
        int [] a={12,34,45,34,34,39};//   integer array contains  integer values
        String [] b={"syed","ilyas","ahmed"};  // String array contains string values
        for(int output:a){     // advanced loop-- "int--datatpe-output" " output-output variable name"  "input-a"
            System.out.println(output);
        }
      for (String name:b){     // advance loop -- "String--datatype-output"  "name-output variable name " "input-b"

          System.out.println(name);
      }
    }
}
